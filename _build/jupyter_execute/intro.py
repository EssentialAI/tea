# ## Why Math is important for Machine Learning (and AI in general)?
# 
# There are a plethora of online courses on Machine Learning and AI, yet, truly comprehending the traits of AI has been a very difficult task. This is because several online courses teach Machine Learning as if it's just a **_plug-and-play game_**. One cannot thrive in the field of Machine Learning without appreciating the Math involved. It is widely believed that mathematical intuition is essential for comprehending the current State of The Art (SoTA) models. 
